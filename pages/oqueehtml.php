<h2>Os Elementos HEAD e TITLE</h2>
<p>Dentro do par "<"heade">" ... "<"/heade">" fica o cabeçalho que contém informações gerais sobre o documento inteiro. O mais simples é o que compoe o título da página <br>Esse título aparecerá na barra superior do naveador e deve ser delimitado pelas etiquetas TITLE, como mostra a figura</p>
<img src="img/titulododocumento.png"><br>
<h2>O elemento BODY</h2>
<p>O texto contido na delimitação do par BODY é o texto que será apresentado ao leitor, mostrado na janela principal do navegador. Esse corpo pode conter títulos de itens e subitens (cabeçalhos), parágrafos, listas, tabelas, formulários, ligações (links) para outros documento, imagens, sons etc.</p>
