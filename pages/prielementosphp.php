<h2>Principais Elementos do PHP</h2>
<p>As principais tags utilizadas no PHP são: </p>
<ul>
	<li>
		PHP: Todo e qualquer comando tem que estar dentro da tag "<"?php ... ">", senão o código não é entendido pelo servidor
	</li>
	<li>
		Echo: Quando você precita imprimir uma frase na tela, utiliza-se o comando echo.
		<br>
		<img src="img/comandoecho.png">
	</li>
	<li>
		Variavel: o PHP como qualquer linguagem de programação, utiliza de váriaveis, para guardar valores ou receber valores inseridos pelo usuário, esses valores são guardados através de um nome iniciado com "$", no PHP tudo que inicia com o SIFRÃO ("$") significa que se trata de uma váriavel.
	</li>
</ul>



