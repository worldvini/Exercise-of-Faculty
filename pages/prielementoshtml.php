<h2>Principais Elementos</h2>
<p>As principais tags do HTMl, que são mais utilizadas são as seguintes</p>
<ul>
	<li>
		Head : onde ficam as informações sobre o site, como titulo ou links para arquivos de outras paginas.
		<ul>
			<li>
				Title: nome do site que vai mostrar na barra ou aba do navegador.
			</li>
			<li>
				Link: que vai linkar um arquivo externo, como css, script ou outras fontes que serão inseridos no seu texto.
			</li>
			<li>
				Meta: usado para inserir qual tipo de cactere vindo do teclado a sua página ira interpretar.
			</li>
		</ul>
	</li>
	
	<li>
		Body: é onde vão todas as informações do seu site que vão aparecer para o cliente/usuário do seu site.
		<ul>
			<li>
				H1 ao H6: são os titulos do tipo cabeçalho intituladores, servem para dar uma hierarquia nos titulos do site,do maior para o mais fraco.
			</li>
			<li>
				P: são os paragrafos, todo o texto que está dentro dessa tag é considerado um unico parágrafo.
			</li>
			<li>
				br: esses não tem tag de fechamento, servem para da uma quebra de linha sem provocar espaçamento entre os parágrafos.
			</li>
			<li>
				A: é o link, permitindo o desvio para uma região do documento correndo, outro documento ou outra pagina da web.
			</li>
		</ul>
	</li>
	
	
</ul>