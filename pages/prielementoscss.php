<h2>Principais Elementos do CSS</h2>
<p>O CSS pode alterar cor, fonte, espaçamento e etc. Resumindo, ele pode estilizar todo o conteudo do site o deixando mais bonito e imcorpado, mas para que isso sejá possivel quando ele está vindo de algum outro arquivo ele tem que estilizar apontando para o seguintes itens:</p>
<ul>
	<li>
		Elemento: ele estiliza o elemento chamando pelo seu nome e alterando suas propriedades, como exemplo:<pre> P{}, BODY{}, FIGURE{}, A{} </pre> 
	</li>
	<li>
		Classe: você cria uma classe no html, e chama essa classe no css através do nome da classe com um ponto na frente  e assim muda as propriedades dela, então todo e qualquer objeto que tiver essa classe no html, vão sofrer as modificações. 
		<pre>.nomedaclasse{} </pre>
	</li>
	<li>
		ID: segue quase o mesmo sistema da classe, só que diferente da classe que pode ser aplicada em diversor elementos, o ID é unico, e só pode ser aplicado em um unico elemento no HTML e não pode ter repetições. Você pode personalizar suas propriedades chamando ele no CSS com a Hashtag e o nome do ID, como exemplo:  <pre>#nomedoID{}</pre>
	</li>
</ul>